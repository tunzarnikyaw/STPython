acronym = input("Enter an acronym: ").upper()

if acronym == "WWW":
    print("World Wide Web")
elif acronym == "USB":
    print("Universal Serial Bus")
elif acronym == "CPU":
    print("Central Processing Unit")
elif acronym == "RAM":
    print("Random Access Memory")
elif acronym == "OS":
    print("Operating System")
else:
    print("Unknown Acronym")
